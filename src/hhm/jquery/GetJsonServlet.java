	/**
	 * The doGet method of the servlet. <br>
	 * 
	 * This method is called when a form has its tag value method equals to get.
	 * 
	 * @param request
	 *            the request send by the client to the server
	 * @param response
	 *            the response send by the server to the client
	 * @throws ServletException
	 *             if an error occurred
	 * @throws IOException
	 *             if an error occurred
	 */
	public void doGet(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {
		request.setCharacterEncoding("utf-8");
		response.setContentType("application/json; charset=UTF-8");
		PrintWriter out = response.getWriter();

		List<Comment> list = new ArrayList<Comment>();

		Comment comment = new Comment();
		comment.setComment_content("这双鞋子质量实在是太好了");
		comment.setComment_parent_id("0");
		comment.setComment_time("2014-05-06 10:15");
		comment.setComment_id(1);
		comment.setUser_id(1);
		comment.setUser_image("www.baidu.com");
		comment.setUsername("科比");

		list.add(comment);

		JSONArray jsonObj = JSONArray.fromObject(list);
		out.println(jsonObj.toString());
		System.out.println(jsonObj.toString());
		out.flush();
		out.close();
	}

